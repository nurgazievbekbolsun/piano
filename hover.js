function setColor(a){
    let color = randomColor
    a.style.background = randomColor()
        a.style.boxShadow = `2px 2px 3px ${color},0 0 10px ${color}`
    a.style.transform = 'scale(1.2)'
}
function removeColor(b){
    b.style.background = '#777474';
    b.style.transform = 'scale(1)'
    b.style.boxShadow = '0 0 2px #000'
}
function randomColor(){
const idx = Math.round(Math.random() * colorNames.length)
return colorNames[idx]
}


function myFunction() {
    if (audio.paused) {
        audio.play();
        btn.style.display = "none";
        // btn.innerHTML = "Pause";
    } else {
        audio.pause();
        // btn.innerHTML = "Play";
        btn.style.display = "none";
    }
}
